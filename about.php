		    <p><strong>Made by Hand was created</strong> out of the belief that the things we collect, consume, use and share are part of who we are as individuals.  For example, the food that we eat says something about each of us.  Objects that surround the space we dwell in tell stories too, not just about us.  Where did they come from?  Who made them and how were they made?</p> 
		    <p>Made by Hand grew out of a need to tell some of these stories and to celebrate the human connection between those who provide services and make goods with those of us using them.  It's about people.  The project is also about celebrating the great traditions of design, craft and manufacturing.  Through our portrait films, we aim to promote that which is made locally, sustainably, and with love from individual proprietors and small businesses.  And we hope that you find the spirit of all of this inspiring.</p>
		    
